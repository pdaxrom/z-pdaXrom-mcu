;
; HJ12864-COG-5
;
; GPIO 3 - PIN_RS
; GPIO 4 - PIN_RSTB
; MSS    - PIN_CSB
; MOSI   - PIN_SDA
; MSCK   - PIN_SCLK
;

PIN_RS		equ	$08
PIN_RSTB	equ	$10

	include ../DEVMAP.INC

	include ../BOOTMEM.INC

	include ../BOOTROM.INC

	org $100

	sei

	lds	#$7ff

	ldx	#hello
;	int	F_PUTSTR
	jsr	F_UART_PUTS

	bsr	disp_init

	ldx	#heart
	jsr	lcd_show

	jmp	F_RESET

send_byte proc
;	jsr	printhex
busy	ldab	SPI_STATUS
	bitb	#SPI_READY
	beq	busy
	staa	SPI_DATA+1
	rts
	endp

send_cmd proc
;	jsr	printhex
	pshb
	ldab	GPIO_DATA+3
	andb	#$FF^PIN_RS
	stab	GPIO_DATA+3
	bsr	send_byte
	pulb
	rts
	endp

send_data proc
;	jsr	printhex
	pshb
	ldab	GPIO_DATA+3
	orab	#PIN_RS
	stab	GPIO_DATA+3
	bsr	send_byte
	pulb
	rts
	endp

delay	proc
	pshb
	psha
loop	ldab	#110
loop1	decb
	bne	loop1
	deca
	bne	loop
	pula
	pulb
	rts
	endp

disp_init proc
	ldaa	#(PIN_RS | PIN_RSTB)
	staa	GPIO_DIR+3

	ldaa	#2
	staa	SPI_PRESCALER
	ldaa	#SPI_SSM
	staa	SPI_CONFIG

	ldaa	GPIO_DATA+3
	anda	#$FF^PIN_RSTB
	staa	GPIO_DATA+3

	ldaa	#20
	bsr	delay

	ldab	GPIO_DATA+3
	orab	#PIN_RSTB
	stab	GPIO_DATA+3

	ldaa	#0
	staa	SPI_CONFIG

	ldaa	#20
	bsr	delay

	ldx	#init1
loop1	ldaa	0,x
	bsr	send_cmd

	ldaa	#50
	bsr	delay

	inx
	cpx	#init1+4
	bne	loop1

	ldx	#init2
loop2	ldaa	0,x
	bsr	send_cmd

	inx
	cpx	#init2+8
	bne	loop2

	rts
init1	db	$e2, $2c, $2e, $2f
init2	db	$24, $81, $1f, $a2, $c8, $a0, $60, $af
	endp

lcd_address proc
	psha
	pshb
	deca
	adda	#$B0
	jsr	send_cmd
	tba
	lsra
	lsra
	lsra
	lsra
	adda	#$10
	jsr	send_cmd
	tba
	anda	#$0F
	jsr	send_cmd
	pulb
	pula
	rts
	endp

lcd_show proc
	pshy
	pshb
	ldy	#ROT8x8_IO
	ldaa	#0
loop	psha
busy1	ldab	SPI_STATUS
	bitb	#SPI_READY
	beq	busy1
	adda	#$B0
	jsr	send_cmd
	ldaa	#$10
	jsr	send_cmd
	ldaa	#$01
	jsr	send_cmd

busy2	ldab	SPI_STATUS
	bitb	#SPI_READY
	beq	busy2

	ldaa	#16
loop1	psha
	ldaa	$00,x
	staa	0,y
	ldaa	$10,x
	staa	1,y
	ldaa	$20,x
	staa	2,y
	ldaa	$30,x
	staa	3,y
	ldaa	$40,x
	staa	4,y
	ldaa	$50,x
	staa	5,y
	ldaa	$60,x
	staa	6,y
	ldaa	$70,x
	staa	7,y
	ldaa	0,y
	jsr	send_data
	ldaa	1,y
	jsr	send_data
	ldaa	2,y
	jsr	send_data
	ldaa	3,y
	jsr	send_data
	ldaa	4,y
	jsr	send_data
	ldaa	5,y
	jsr	send_data
	ldaa	6,y
	jsr	send_data
	ldaa	7,y
	jsr	send_data
	inx
	pula
	deca
	bne	loop1

	ldab	#128-16
	abx

;	jsr	F_UART_IN

	pula
	inca
	cmpa	#8
	beq	exit
	jmp	loop
exit	pulb
	puly
	rts
	endp

printhex proc
	pshx
	psha
	pshb
	jsr	F_UART_PUTHEX
	tba
	jsr	F_UART_PUTHEX
	ldx	#crlf
	jsr	F_UART_PUTS
	pulb
	pula
	pulx
	rts
crlf	db	10, 13, 0
	endp

